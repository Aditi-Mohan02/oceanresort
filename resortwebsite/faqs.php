<button class="accordion">WHAT ARE THE NEARBY GOA AIRPORTS?</button>
<div class="panel">
  <pre>
    A: GOA is served by three major airports and three smaller suburban airports.
    LaGuardia (LGA) is the closest to Midtown and Times Square but is mostly a domestic airport,
    with some flights to and from Canada. It has the least convenient mass transit connections,
    but metered taxis are readily available and relatively cheap. John F. Kennedy International
    (JFK) is New York's largest airport and the top for international flights, but it is a bit
    further away from Manhattan than LGA and the traffic is often heavy and unpredictable.
    Newark-Liberty International (EWR) in New Jersey is the closest to lower Manhattan and
    does offer International flights. Flying through Newark can be cheaper, but you'll pay
    more for car services, taxi and Uber..</pre>
</div>
